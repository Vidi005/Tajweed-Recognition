const removeNonArabic = text => {
  const arabicRegex = /[\u0600-\u06FF\u0750-\u077F\u08A0-\u08FF]/gm
  if (arabicRegex.test(text)) {
    /* (2nd) replace is used to replace certain madd "\u0653" diacritics which is not working after alif in regex as an alternative solution */
    return text.replace(/[^\u0600-\u06FF\u0750-\u077F\u08A0-\u08FF\s]/gm, '').replace(/\u0627\u0653(?!\s*[\u0621-\u0627]|\s*[\u0648\u0649][\u0654\u0655])/gm, '\u0627\u06E4')
  } else {
    return ''
  }
}

const buildRegExp = rules => {
  const isRegexArray = rules.every(rule => rule instanceof RegExp)
  if (isRegexArray) {
    return new RegExp(`${rules.map(regex => regex.source).join('|')}`, 'gm')
  } else {
    return new RegExp(`(${rules.join('|')})`, 'gm')
  }
}

const colorizeChars = (recognizedText, tajweedLaws, colorizationMode, isOddPosition) => {
  /* Replace is used to replace certain madd "\u0653" diacritics which is not working after alif in regex as an alternative solution */
  let colorizedChars = recognizedText.replace(/\u0627\u0653(?!\s*[\u0621-\u0627]|\s*[\u0648\u0649][\u0654\u0655])/gm, '\u0627\u06E4')
  if (colorizedChars.includes('\u06DB')) {
    const applyOptionalColor = (part, regex, id, color) => {
      if (colorizationMode === 'Text Color') {
        return part.replace(regex, match => `<span class="tajweed-${id}" style="color: ${color}; cursor: pointer;">${match}</span>`)
      } else {
        return part.replace(regex, match => `<span class="tajweed-${id}" style="background-color: ${color}BF; cursor: pointer;">${match}</span>`)
      }
    }
    const parts = colorizedChars.split('\u06DB')
    const waqfTajweedLaws = waqfMuanaqohContinuityTajweedLaws(tajweedLaws)    
    colorizedChars = parts.map((part, i) => {
      let relevantLaws = (i % 2 === 0) ? waqfTajweedLaws : tajweedLaws
      isOddPosition ? relevantLaws = (i % 2 !== 0) ? waqfTajweedLaws : tajweedLaws : relevantLaws = (i % 2 === 0) ? waqfTajweedLaws : tajweedLaws
      relevantLaws.forEach(tajweedLaw => {
        tajweedLaw.rules.forEach(rule => {
          const regex = (typeof rule === 'string') ? new RegExp(`(${rule})`, 'gm') : rule
          part = applyOptionalColor(i >= parts.length - 1 ? part : `${part}\u06DB`, regex, tajweedLaw.id, tajweedLaw.color)
        })
      })      
      return part.replace(/\u06DB+/gm, '\u06DB')
    }).join('')
  } else {
    const applyColor = (regex, id, color) => {
      if (colorizationMode === 'Text Color') {
        colorizedChars = colorizedChars.replace(regex, match => `<span class="tajweed-${id}" style="color: ${color}; cursor: pointer;">${match}</span>`)
      } else {
        colorizedChars = colorizedChars.replace(regex, match => `<span class="tajweed-${id}" style="background-color: ${color}BF; cursor: pointer;">${match}</span>`)
      }
    }
    tajweedLaws.forEach(tajweedLaw => {
      tajweedLaw.rules.forEach(rule => {
        const regex = (typeof rule === 'string') ? new RegExp(`(${rule})`, 'gm') : rule
        applyColor(regex, tajweedLaw.id, tajweedLaw.color)
      })
    })
  }
  return colorizedChars
}

const checkParamEvent = (idParam, tajweedLaws) => {
  if (typeof idParam !== 'number') {
    const event = idParam
    const matchedTajweed = tajweedLaws.filter(tajweedLaw => {
      const regex = buildRegExp(tajweedLaw.rules)
      return regex.test(event.target.innerHTML)
    })
    if (matchedTajweed.length === 1) {
      return matchedTajweed
    }
  } else {
    const tajweedId = idParam
    return tajweedLaws.filter(tajweedLaw => tajweedLaw.id === tajweedId) || []
  }
}

